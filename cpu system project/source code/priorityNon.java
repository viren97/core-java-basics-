    private void btngetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btngetActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model =(DefaultTableModel) table.getModel();
        int at[]=new int [model.getRowCount()];
        int bt[]=new int [model.getRowCount()];
        int ct[]=new int [model.getRowCount()];
        int wt[]=new int [model.getRowCount()];
        int tat[]=new int [model.getRowCount()];
        int pid[]=new int [model.getRowCount()];
           int f[]=new int [model.getRowCount()];
              int p[]=new int [model.getRowCount()];
        int n=model.getRowCount();
        for(int i=0; i<model.getRowCount(); i++)
        {
            pid[i]=Integer.parseInt(model.getValueAt(i,0).toString());
            at[i]=Integer.parseInt(model.getValueAt(i,1).toString());
            bt[i]=Integer.parseInt(model.getValueAt(i,2).toString());
            f[i]=0;
             p[i]=Integer.parseInt(model.getValueAt(i,3).toString());
        }
          int tot=0,st=0,temp = 0,flag=0;
  
		while(true)
		{
			int c=n, max=0;
			if (tot == n) // total no of process = completed process loop will be terminated
				break;
			// i'th time wale  processes  ka burst time min calculate karna  
			for (int i=0; i<n; i++)
			{ 
                            
				/*
				 * If i'th process arrival time <= system time and its flag=0 and burst<min 
				 * That process will be executed first 
				 */ 
				if ((at[i] <= st) && (f[i] == 0) && (p[i]>max))
				{
					max=p[i];
					c=i;
                    flag++;
				}
                         
                        }     // jo aa chuka hai aur jiska burst time min hai wo process yaha se select hoga 
			
			/* If c==n means c value can not updated because no process arrival time< system time so we increase the system time */
			if (c==n) 
				st++;
			else
			{
                            if(flag==1)
                            {ct[c]=at[c]+bt[c];
                            temp=ct[c];
                            }
                            else
                            {
                                 ct[c]=temp+bt[c];
                                  temp=ct[c];
                            }
				tat[c]=ct[c]-at[c];
				wt[c]=tat[c]-bt[c];
				f[c]=1;
				tot++;
                                st+=bt[c];
			}
		
		

                }
           for(int i=0; i<model.getRowCount(); i++)
                {  model.setValueAt(at[i], i,1);
                  model.setValueAt(pid[i], i,0);
                  model.setValueAt(bt[i], i,2);
                    model.setValueAt(p[i], i,3);
                     model.setValueAt(ct[i], i,4);
                      model.setValueAt(tat[i], i,5);
                          model.setValueAt(wt[i], i,6);
                }
 
    }//GEN-LAST:event_btngetActionPerformed

